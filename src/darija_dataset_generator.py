def _apply_word_substitutions(text: str) -> str:
    """Applique des substitutions alรฉatoires de mots Darija."""
    result = text
    for original, alternatives in WORD_SUBSTITUTIONS.items():
        if original in result and random.random() < 0.35:
            result = result.replace(original, random.choice(alternatives), 1)
    return result


def generate_variation(text: str) -> str:
    """Gรฉnรจre une variation d'un commentaire avec 7 stratรฉgies."""
    variation = text
    strategy = random.random()

    if strategy < 0.15:
        variation = _apply_word_substitutions(variation)
    elif strategy < 0.30:
        prefix = random.choice(PREFIXES)
        variation = prefix + variation
    elif strategy < 0.45:
        suffix = random.choice(SUFFIXES)
        variation = variation + suffix
    elif strategy < 0.55:
        reaction = random.choice(REACTIONS)
        variation = reaction + " " + variation
    elif strategy < 0.65:
        emoji = random.choice(DARIJA_EMOJIS)
        if random.random() < 0.5:
            variation = emoji + " " + variation
        else:
            variation = variation + " " + emoji
    elif strategy < 0.75:
        agreement = random.choice(AGREEMENT_PHRASES)
        variation = agreement + " " + variation
    else:
        variation = _apply_word_substitutions(variation)
        if random.random() < 0.5:
            variation = variation + " " + random.choice(DARIJA_EMOJIS)

    return variation.strip()


def generate_combined_comment() -> str:
    """Gรฉnรจre un commentaire en combinant des fragments de thรจmes diffรฉrents."""
    groups = random.sample(ALL_COMMENTS_GROUPS, min(3, len(ALL_COMMENTS_GROUPS)))
    parts = []
    for _, comments in groups:
        parts.append(random.choice(comments))

    r = random.random()
    if r < 0.3 and len(parts) >= 2:
        connector = random.choice(CONNECTORS)
        return parts[0] + connector + parts[1]
    elif r < 0.45 and len(parts) >= 3:
        short_parts = [p for p in parts if len(p.split()) < 10]
        if len(short_parts) >= 2:
            connector = random.choice(CONNECTORS)
            return short_parts[0] + connector + short_parts[1]
    elif r < 0.6 and len(parts) >= 2:
        reaction = random.choice(REACTIONS)
        return reaction + " " + parts[0]

    return parts[0]


def _text_hash(text: str) -> str:
    """Hash rapide pour dรฉtecter les doublons."""
    return hashlib.md5(text.encode('utf-8')).hexdigest()


def generate_massive_dataset(target_size: int = 10000,
                              seed: int = 42) -> Generator[Dict[str, str], None, None]:
    """
    Gรฉnรฉrateur de dataset massif de commentaires Darija.
    Utilise la dรฉduplication inline par hash MD5.

    Args:
        target_size: Nombre de commentaires ร gรฉnรฉrer
        seed: Seed pour la reproductibilitรฉ

    Yields:
        Dict {"text": ..., "url": ...}
    """
    random.seed(seed)
    base_comments = get_all_base_comments()
    seen_hashes: Set[str] = set()
    generated = 0
    attempts = 0
    max_attempts = target_size * 5

    while generated < target_size and attempts < max_attempts:
        attempts += 1
        r = random.random()

        if r < 0.25:
            text = random.choice(base_comments)
        elif r < 0.45:
            text = generate_variation(random.choice(base_comments))
        elif r < 0.65:
            text = generate_combined_comment()
        elif r < 0.80:
            text = generate_variation(random.choice(base_comments))
            text = generate_variation(text)
        elif r < 0.90:
            text = generate_variation(generate_combined_comment())
        else:
            text = generate_variation(
                generate_variation(
                    generate_variation(random.choice(base_comments))
                )
            )

        text = text.strip()
        if not text or len(text) < 3:
            continue

        h = _text_hash(text)
        if h in seen_hashes:
            continue
        seen_hashes.add(h)

        url = random.choice(FAKE_URLS)
        yield {"text": text, "url": url}
        generated += 1


def generate_dataset_list(target_size: int = 10000, seed: int = 42) -> List[Dict[str, str]]:
    """Gรฉnรจre une liste complรจte de commentaires Darija."""
    return list(generate_massive_dataset(target_size, seed))
